import { Router } from "express";
import { addProduct, getProducts, deleteProduct, updateProduct, getProductById } from "./product.controller.js";
import { addProductValidator, deleteProductValidator, updateProductValidator, getProductByIdValidator } from "../middlewares/product-validator.js";

const router= Router();

/**
 * @swagger
 * /products/addProduct:
 *   post:
 *     summary: Add a new product
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             $ref: '#/components/schemas/Product'
 *     responses:
 *       201:
 *         description: Product added successfully
 */
router.post("/addProduct",addProductValidator, addProduct);

/**
 * @swagger
 * /products:
 *   get:
 *     summary: Retrieve a list of products
 *     responses:
 *       200:
 *         description: A list of products
 *         content:
 *           application/json:
 *             schema:
 *               type: array
 *               items:
 *                 $ref: '#/components/schemas/Product'
 */
router.get("/",getProducts);

/**
 * @swagger
 * /products/deleteProduct/{id}:
 *   delete:
 *     summary: Delete a product by ID
 *     parameters:
 *       - in: path
 *         name: id
 *         required: true
 *         schema:
 *           type: string
 *         description: The product ID
 *     responses:
 *       200:
 *         description: Product deleted successfully
 *       404:
 *         description: Product not found
 */
router.delete("/deleteProduct/:id", deleteProductValidator, deleteProduct);

/**
 * @swagger
 * /products/editProduct/{id}:
 *   put:
 *     summary: Update a product by ID
 *     parameters:
 *       - in: path
 *         name: id
 *         required: true
 *         schema:
 *           type: string
 *         description: The product ID
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             $ref: '#/components/schemas/Product'
 *     responses:
 *       200:
 *         description: Product updated successfully
 *       404:
 *         description: Product not found
 */
router.put("/editProduct/:id", updateProductValidator, updateProduct);

/**
 * @swagger
 * /products/findProduct/{id}:
 *   get:
 *     summary: Retrieve a product by ID
 *     parameters:
 *       - in: path
 *         name: id
 *         required: true
 *         schema:
 *           type: string
 *         description: The product ID
 *     responses:
 *       200:
 *         description: Product retrieved successfully
 *       404:
 *         description: Product not found
 */
router.get("/findProduct/:id", getProductByIdValidator, getProductById);

export default router;